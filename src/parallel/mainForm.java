    public static int[] llenarArreglo(int arreglo[]) {

        ArrayList<Integer> arrl = new ArrayList<Integer>();
        int n = 0;
        for (int i = 0; i < arreglo.length; i++) {
            do {
                n = (int) (Math.random() * arraylength);

            } while (arrl.contains(n));
            arrl.add(n);
            arreglo[i] = (int) (Math.random() * arraylength);
        }

        return arreglo;
    }

